import React, { useEffect, useState, useRef } from "react";

import Head from "next/head";
import { Container, Tab, Nav } from "react-bootstrap";

import Navbar from "@/components/Common/Navbar";
import Footer from "@/components/Common/Footer";
import MapMain from "@/components/Map";
import SocialMedia from "@/components/Common/SocialMedia";
import Simulation from "@/components/Simulation";
import Combatants from "@/components/Combatants";

const projectTabs = [
  {
    title: "Compatatnt Selecttion",
  },
  {
    title: "Map Selection",
  },
  {
    title: "Simulation",
  },
];

export default function Home() {
  const [refresh, doRefresh] = useState(0);

  const [activeTab, setActiveTab] = useState(projectTabs[0].title);
  const [backButtonVisible, setBackButtonVisible] = useState(true);
  const [nextButtonVisible, setNextButtonVisible] = useState(true);
  const isInitialLoad = useRef(true);

  const handleTabChange = (selectedTab) => {
    setActiveTab(selectedTab);

    // Update button visibility based on the selected tab
    setBackButtonVisible(
      projectTabs.findIndex((tab) => tab.title === selectedTab) !== 0
    );
    setNextButtonVisible(
      projectTabs.findIndex((tab) => tab.title === selectedTab) !==
        projectTabs.length - 1
    );
  };

  const handleNext = () => {
    const currentIndex = projectTabs.findIndex(
      (tab) => tab.title === activeTab
    );
    const nextIndex = (currentIndex + 1) % projectTabs.length;
    const nextTab = projectTabs[nextIndex].title;

    setActiveTab(nextTab);

    // Hide the "Next" button if on the last tab
    if (nextIndex === projectTabs.length - 1) {
      setNextButtonVisible(false);
    }

    // Always show the "Back" button after clicking "Next"
    setBackButtonVisible(true);
  };

  const handleBack = () => {
    const currentIndex = projectTabs.findIndex(
      (tab) => tab.title === activeTab
    );
    const previousIndex =
      (currentIndex - 1 + projectTabs.length) % projectTabs.length;
    const previousTab = projectTabs[previousIndex].title;

    setActiveTab(previousTab);

    // Hide the "Back" button if on the first tab
    if (previousIndex === 0) {
      setBackButtonVisible(false);
    }

    // Always show the "Next" button after clicking "Back"
    setNextButtonVisible(true);
  };

  useEffect(() => {
    // On the initial load, hide the "Back" button
    if (isInitialLoad.current) {
      setBackButtonVisible(false);
      isInitialLoad.current = false;
    }
  }, []);

  return (
    <>
      <Head>
        <title>Encountera</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <Navbar />
        <div className="d-none d-lg-block">
          <SocialMedia />
        </div>
        <div className="ts-main-content">
          <Container fluid className="ts-container">
            <Tab.Container
              id="GiveAwayTabs"
              defaultActiveKey={projectTabs[0].title}
              activeKey={activeTab}
              onSelect={handleTabChange}
            >
              <div className="d-flex justify-content-center mb-08">
                <Nav variant="pills" className="ts-tabs ts-tabs-primary">
                  {projectTabs.map((tabItem, index) => {
                    return (
                      <Nav.Item className="text-center" key={index}>
                        <Nav.Link eventKey={tabItem.title} role="button">
                          <span> {tabItem.title} </span>
                        </Nav.Link>
                      </Nav.Item>
                    );
                  })}
                </Nav>
              </div>

              <Tab.Content>
                <Tab.Pane eventKey={projectTabs[0].title}>
                  <Combatants />
                </Tab.Pane>
                <Tab.Pane eventKey={projectTabs[1].title}>
                  <MapMain />
                </Tab.Pane>
                <Tab.Pane eventKey={projectTabs[2].title}>
                  <Simulation refresh={refresh} />
                </Tab.Pane>
              </Tab.Content>
            </Tab.Container>
            <div className="d-flex justify-content-center gap-4 mt-5">
              {backButtonVisible && (
                <button
                  className="btn ts-btn ts-btn--lg fw-bold ts-btn-outline-secondary"
                  onClick={handleBack}
                >
                  BACK
                </button>
              )}
              {nextButtonVisible && (
                <button
                  className="btn ts-btn ts-btn--lg fw-bold ts-btn-primary"
                  onClick={handleNext}
                >
                  Next
                </button>
              )}

              {activeTab === projectTabs[2].title && (
                <button
                  className="btn ts-btn ts-btn--lg fw-bold ts-btn-primary"
                  onClick={() => doRefresh((prev) => prev + 1)}
                >
                  Simulate
                </button>
              )}
            </div>
          </Container>
        </div>
        <Footer />
      </main>
    </>
  );
}
